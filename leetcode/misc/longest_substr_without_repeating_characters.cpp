int lengthOfLongestSubstring(string s) {
  int n = s.length(), x = 0, y = 0, ans = 0;
  vector<int> cnt(256, 0);
  while (x <= y && y < n) {
    if (cnt[s[y]] == 1) {
      cnt[s[x]]--;
      x++;
    } else {
      cnt[s[y]]++;
      ans = max(ans, y - x + 1);
      y++;
    }
  }
  return ans;
}

