long long uniquePathsWithObstacles(vector<vector<int>> &arr) {
  long long n = arr.size(), m = arr[0].size();
  long long dp[n][m];
  if (arr[n - 1][m - 1] == 1) {
    dp[n - 1][m - 1] = 0;
  } else {
    dp[n - 1][m - 1] = 1;
  }

  for (long long i = n - 1; i >= 0; i--) {
    for (long long j = m - 1; j >= 0; j--) {
      if (i == n - 1 && j == m - 1) continue;
      if (arr[i][j] == 1) {
        dp[i][j] = 0;
      } else {
        long long s = 0;
        if (i + 1 < n && arr[i + 1][j] == 0) {
          s += dp[i + 1][j];
        }
        if (j + 1 < m && arr[i][j + 1] == 0) {
          s += dp[i][j + 1];
        }
        dp[i][j] = s;
      }
    }
  }

  return dp[0][0];
}

