int jump(vector<int> &nums) {
  if (nums.size() == 1) {
    return 0;
  }
  int mi = nums[0], n = nums.size(), ci = 0, ans = 1, i = 1;
  while (i < n) {
    if (i <= mi) {
      ci = max(ci, i + nums[i]);
      i++;
    } else {
      mi = ci;
      ans++;
      ci = 0;
    }
  }
  return ans;
}

