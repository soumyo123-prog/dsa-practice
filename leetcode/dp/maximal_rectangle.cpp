vector<int> getPse(vector<int> h, int n) {
  vector<int> pse(n, -1);
  stack<int> st;
  for (int i = n - 1; i >= 0; i--) {
    if (st.empty()) {
      st.push(i);
      continue;
    }
    if (h[st.top()] <= h[i]) {
      st.push(i);
    } else {
      while (!st.empty() && h[st.top()] > h[i]) {
        pse[st.top()] = i;
        st.pop();
      }
      st.push(i);
    }
  }
  return pse;
}

vector<int> getNse(vector<int> h, int n) {
  vector<int> nse(n, n);
  stack<int> st;
  for (int i = 0; i < n; i++) {
    if (st.empty()) {
      st.push(i);
      continue;
    }
    if (h[st.top()] <= h[i]) {
      st.push(i);
    } else {
      while (!st.empty() && h[st.top()] > h[i]) {
        nse[st.top()] = i;
        st.pop();
      }
      st.push(i);
    }
  }
  return nse;
}

int maxRectHistogram(vector<int> h, int n) {
  vector<int> nse = getNse(h, n), pse = getPse(h, n);
  int ans = 0;
  for (int i = 0; i < n; i++) {
    int r = nse[i], l = pse[i];
    ans = max(ans, (r - l - 1) * (h[i]));
  }
  return ans;
}

int maximalRectangle(vector<vector<char>> &arr) {
  int n = arr.size(), m = arr[0].size(), ans = 0;
  vector<int> h(m, 0);
  for (int i = 0; i < n; i++) {
    for (int j = 0; j < m; j++) {
      if (arr[i][j] == '1') {
        h[j]++;
      } else {
        h[j] = 0;
      }
    }
    ans = max(ans, maxRectHistogram(h, m));
  }
  return ans;
}

