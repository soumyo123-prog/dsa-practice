int jump(vector<int>& nums) {
  int n = nums.size();
  vector<int> dp(n, -1);
  dp[0] = 0;
  int mi = nums[0], cmi = -1, curr = 1, i = 1;
  while (i < n) {
    if (i <= mi) {
      dp[i] = curr;
      cmi = max(cmi, min(i + nums[i], n - 1));
      i++;
    } else {
      curr++;
      mi = cmi;
      cmi = -1;
    }
  }
  return dp[n - 1];
}

