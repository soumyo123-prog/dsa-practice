int maxArea(vector<int> arr) {
  int n = arr.size();
  vector<pair<int, int>> nums;
  for (int i = 0; i < n; i++) {
    nums.push_back(make_pair(arr[i], i));
  }
  sort(nums.begin(), nums.end());
  set<int> st;
  for (int i = 0; i < n; i++) {
    st.insert(nums[i].second);
  }

  int ans = 0;
  for (int i = 0; i < n - 1; i++) {
    st.erase(st.find(nums[i].second));
    int d1 = abs(nums[i].second - *st.begin());
    int d2 = abs(nums[i].second - *st.rbegin());
    ans = max(ans, max(nums[i].first * d1, nums[i].first * d2));
  }

  return ans;
}

