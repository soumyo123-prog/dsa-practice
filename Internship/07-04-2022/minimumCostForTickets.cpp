int mincostTickets(vector<int> days, vector<int> costs) {
  int dp[366] = {0};
  dp[365] = costs[0];
  for (int i = 364; i >= 0; i--) {
    if (i >= 359) {
      dp[i] = dp[i + 1] + costs[0];
    } else if (i >= 336) {
      dp[i] = min(dp[i + 1] + costs[0], dp[i + 7] + costs[1]);
    } else {
      dp[i] = min(dp[i + 1] + costs[0], dp[i + 7] + costs[1],
                  dp[i + 30] + costs[2]);
    }
  }
}

