vector<int> dwtc(string exp, map<string, vector<int>> &dp) {
  if (dp.find(exp) != dp.end()) {
    return dp[exp];
  }

  vector<int> res;
  for (int i = 0; i < exp.size(); i++) {
    if (exp[i] == '+' || exp[i] == '-' || exp[i] == '*') {
      vector<int> v1 = dwtc(exp.substr(0, i), dp);
      vector<int> v2 = dwtc(exp.substr(i + 1), dp);
      for (int j = 0; j < v1.size(); j++) {
        for (int k = 0; k < v2.size(); k++) {
          if (exp[i] == '+') {
            res.push_back(v1[j] + v2[k]);
          } else if (exp[i] == '-') {
            res.push_back(v1[j] - v2[k]);
          } else {
            res.push_back(v1[j] * v2[k]);
          }
        }
      }
    }
  }

  if (res.size() == 0) {
    res.push_back(stoi(exp));
  }
  return (dp[exp] = res);
}

vector<int> diffWaysToCompute(string exp) {
  map<string, vector<int>> dp;
  return dwtc(exp, dp);
}

void solve() {
  string exp = "2*3-4*5";
  vint res = diffWaysToCompute(exp);
  printArray(res, res.size());
}

