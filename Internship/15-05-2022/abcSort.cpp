#include <bits/stdc++.h>
using namespace std;
#define endl << "\n"
#define in cin >>
#define out cout <<
#define pb push_back
#define mp make_pair
#define rep(i, a, b) for (int i = a; i < b; i++)
typedef long long ll;
typedef unsigned long long ull;
typedef pair<int, int> pint;
typedef vector<vector<int>> vvint;
typedef vector<int> vint;
typedef vector<pair<int, int>> vpint;
typedef vector<char> vchar;
typedef vector<string> vstring;
typedef map<int, int> mint;
typedef set<int> sint;

void initialize(vector<int> &arr, int n) {
  for (int i = 0; i < n; i++) {
    int x;
    in x;
    arr.pb(x);
  }
}

void printYesOrNo(bool flag) {
  if (flag) {
    out "YES" endl;
  } else {
    out "NO" endl;
  }
}

void solve() {
  int n;
  in n;
  vint a, b, c;
  initialize(a, n);
  for (int i = n - 1; i >= 0; i--) {
    b.push_back(a[i]);
  }
  for (int i = n - 1; i >= 0; i--) {
    if (i % 2 == 0) {
      c.pb(b[i]);
    } else {
      int i1 = b[i], i2 = b[i - 1];
      c.pb(min(i1, i2));
      b[i - 1] = max(i1, i2);
    }
  }
  bool flag = true;
  for (int i = 1; i < n; i++) {
    if (c[i] < c[i - 1]) {
      flag = false;
      break;
    }
  }
  printYesOrNo(flag);
}

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(NULL);
  cout.tie(NULL);
  int t = 1;
  in t;
  while (t--) {
    solve();
  }
  return 0;
}

